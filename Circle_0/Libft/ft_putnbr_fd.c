#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long long	nbr;

	if (fd < 0)
		return ;
	nbr = (long long)n;
	if (nbr < 0)
	{
		ft_putchar_fd('-', fd);
		nbr = -nbr;
	}
	if (nbr >= 10)
	{
		ft_putnbr_fd(nbr / 10, fd);
		ft_putnbr_fd(nbr % 10, fd);
	}
	else
		ft_putchar_fd('0' + nbr, fd);
}
